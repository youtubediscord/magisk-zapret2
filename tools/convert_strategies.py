def generate_html_options(strategies: List[Strategy], include_labels: List[str] = None) -> str:
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç HTML options –¥–ª—è select"""

    # –ì—Ä—É–ø–ø–∏—Ä—É–µ–º –ø–æ —Ç–∏–ø–∞–º
    groups = {
        'Syndata': [],
        'Censorliber': [],
        'Alt': [],
        'Multisplit/Disorder': [],
        'Fake': [],
        'SeqOvl': [],
        'Dronatar': [],
        '–î—Ä—É–≥–∏–µ': []
    }

    for s in strategies:
        # –§–∏–ª—å—Ç—Ä—É–µ–º –ø–æ label –µ—Å–ª–∏ —É–∫–∞–∑–∞–Ω–æ
        if include_labels and s.label and s.label not in include_labels:
            continue

        # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≥—Ä—É–ø–ø—É
        sid = s.id.lower()
        if sid.startswith('syndata'):
            groups['Syndata'].append(s)
        elif sid.startswith('censorliber'):
            groups['Censorliber'].append(s)
        elif sid.startswith('alt'):
            groups['Alt'].append(s)
        elif 'multisplit' in sid or 'multidisorder' in sid or 'disorder' in sid:
            groups['Multisplit/Disorder'].append(s)
        elif sid.startswith('fake'):
            groups['Fake'].append(s)
        elif 'seqovl' in sid:
            groups['SeqOvl'].append(s)
        elif sid.startswith('dronatar') or sid.startswith('dronator'):
            groups['Dronatar'].append(s)
        else:
            groups['–î—Ä—É–≥–∏–µ'].append(s)

    html = ''
    for group_name, group_strategies in groups.items():
        if not group_strategies:
            continue

        html += f'                    <optgroup label="{group_name}">\n'
        for s in group_strategies:
            label_badge = ""
            if s.label == "recommended":
                label_badge = " ‚≠ê"
            elif s.label == "game":
                label_badge = " üéÆ"
            elif s.label == "experimental":
                label_badge = " üß™"

            # –°–æ–∫—Ä–∞—â–∞–µ–º –∏–º—è –µ—Å–ª–∏ —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω–æ–µ
            name = s.name[:40] + "..." if len(s.name) > 40 else s.name
            html += f'                        <option value="{s.id}">{name}{label_badge}</option>\n'
        html += '                    </optgroup>\n'

    return html

